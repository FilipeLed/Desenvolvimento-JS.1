// Exemplo de cadastro de pessoa

// Definir variáveis para cada campo
var nome = "Filipe da Silva"; // string
var salario = 2500.50; // número (ponto flutuante)
var idade = 33; // número (inteiro)
var possuiDiploma = true; // booleano

// Exibir os valores no console
console.log("Nome: " + nome);
console.log("Salário: R$" + salario.toFixed(2)); // Formatando para exibir duas casas decimais
console.log("Idade: " + idade + " anos");
console.log("Possui Diploma: " + (possuiDiploma ? "Sim" : "Não"));
